public class type_promo {
    public static void main(String[] args){
        char a = 'a';
        char b = 'b';
        System.out.println((int)a);
        System.out.println((int)b);
        System.out.println(a);
        System.out.println(a+b);
        int c = 35;
        float d = 45.6f;
        double e = 45;
        System.out.println((int)(c+d+e));
        
    }   
}
