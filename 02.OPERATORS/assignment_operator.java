public class assignment_operator {
    public static void main(String[] args){
        float a = 10f;
        a+= 10;
        System.out.println(a);
        a-=4;
        System.out.println(a);
        a*=4;
        System.out.println(a);
        a/=3;
        System.out.println(a);
        a%=6;
        System.out.println(a);
    }
}
